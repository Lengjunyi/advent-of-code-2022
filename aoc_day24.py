lines = input.split("\n")[1:-1]

h, w = len(lines), len(lines[0]) - 2

lizzards = [set() for _ in range(5)]

for i, line in enumerate(lines):
    for j, c in enumerate(line[1:-1]):
        lizzards[{'^':0,'>':1,'v':2,'<':3,'.':4}[c]].add((j,i))

dxs = [0,1,0,-1,0]
dys = [-1,0,1,0,0]

def navigate(start, end):
    queue = [start]
    visited = set()
    for (x,y,day) in queue:
        for d in range(5):
            nx = x + dxs[d]
            ny = y + dys[d]
            nday = day + 1
            if (nx,ny) == end:
                return nday

            if (nx,ny,nday) in visited:
                continue

            valid = True
            if (nx,ny) != (start[0], start[1]):
                if nx < 0 or nx >= w or ny < 0 or ny >= h:
                    continue
                for ld in range(4):
                    if ((nx-dxs[ld]*nday) % w, (ny-dys[ld]*nday) % h) in lizzards[ld]:
                        valid = False
                        break
            if valid:
                queue.append((nx,ny,nday))
                visited.add((nx,ny,nday))

t1 = navigate((0,-1,0), (w-1,h))
